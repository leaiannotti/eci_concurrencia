type t = C | Shell | XCode

let tags = ["C";"shell";"xcode";]

let parse tag = match Misc.lowercase tag with
| "c" -> Some C
| "shell" -> Some Shell
| "xcode" -> Some XCode
| _ -> None

let pp = function
  | C -> "C"
  | XCode -> "xcode"
  | Shell -> "shell"
